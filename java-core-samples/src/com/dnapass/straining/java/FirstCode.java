package com.dnapass.straining.java;

public class FirstCode {

	public static void main(String[] args) {
		
		//int,float,double
		int i = 5;      //4bytes-->32 bits
		short s = 5;    //2bytes-->16bits
		long raj = 5000000000000l;
		System.out.println(raj+s+i);

		char c = 'A';
		c = 66;
		c++;
		System.out.println(c);
		
		double d1 = (int)5.65; // type casting getting the value in the int format
		System.out.println(d1);
	}

}
